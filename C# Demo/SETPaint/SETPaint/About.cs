    partial class ProgramAbout : Form
    {
        public ProgramAbout()
        {
            InitializeComponent();
            // set attributes.
            this.Text = String.Format("About {0}", "SETPaint");
            this.labelProductName.Text = "Product Name : SETPaint";
            this.labelVersion.Text = String.Format("Version {0}", "1.0.0.0");
